{

     switch(i)
     {
         case 0:
             JOptionPane.showMessageDialog(this,
            "Enter a valid number from 1 to 9",
    "ERROR",
    JOptionPane.ERROR_MESSAGE);
           
         break;
         case 1:  Object[] options = {"Reset",
                    "Close"};
int n = JOptionPane.showOptionDialog(this,"HURRAY You have compleated the game.","SUCCESS",
        JOptionPane.YES_NO_OPTION,
    JOptionPane.QUESTION_MESSAGE,
    null,     
    options,  
    options[0]); 

if (n == JOptionPane.YES_OPTION) {
     new NewJFrame2().setVisible(true);
     dispose();
        }
        
 else if (n == JOptionPane.NO_OPTION) {
    dispose();
    
}
    
         break;
        
         default: JOptionPane.showMessageDialog(this,
    "There are some mistake. Check again",
    "Error",
    JOptionPane.ERROR_MESSAGE);
     }

}
 
     
 
